    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

       //importe condensado

       int ponderacion [] = {7,1,3,7,1,3,7,1,3,7};

       int importe [] = new int [10]; //por la máxima cantidad de dinero 99,000,000.00

       String c;
       
       int suma=0;

       int j=9;

       int lonG = jTextField8.getText().length();

       for (int i=lonG-1; i>=0; i--){
           //c = jTextField8.getText().substring(i, 1);

           c = ""+jTextField8.getText().charAt(i); //se volvió string con el +
                //el charAt obtiene cada valor char en el arreglo

           if(!c.equals(".")){

               importe [j] = Integer.parseInt(c);

               suma = importe [j]* ponderacion [j] + suma;
               j-=1;
           }

        }
       
       suma = suma % 10;
       jTextField6.setText(""+suma);

    }//GEN-LAST:event_jButton2ActionPerformed

